/**
 * @param {number[]} nums
 * @param {number} k
 * @return {number}
 */
var findKthLargest = function(nums, k) {
    const heap = [nums[0]];
    for (let i = 1; i < nums.length; i ++) {
        if (nums[i] >= heap[heap.length - 1]) {
            heap.push(nums[i])
        } else {
            if (heap[0] < nums[i]){
                heap.shift()
                heap.unshift(nums[i])
            }
        }

        if (heap.length > k){
            heap.shift()
        }
    }
    console.log(heap)
    return heap[0];
};
