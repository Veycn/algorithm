var hasCycle = function(head) {
    if (!head || !head.next){
        return false
    }
    var fast = head.next.next, slow = head.next
    while (fast){
        if (fast === slow){
            return true
        }
        fast = fast.next ? fast.next.next : null
        slow = slow.next
    }
    return false
};
